#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*newstr;
	int		i;

	newstr = (char *)malloc(sizeof(char) * ft_strlen(s1));
	i = 0;
	while (s1[i])
	{
		newstr[i] = s1[i];
		i++;
	}
	newstr[i] = '\0';
	return (newstr);
}
