#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if (!(str = (char *)ft_memalloc(sizeof(char) * size + 1)))
		return (NULL);
	ft_memset(str, '\0', size);
	return (str);
}
