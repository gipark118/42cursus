#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*newstr;
	int		i;

	if (!(newstr = (char *)malloc(sizeof(char) * ft_strlen(str) + 1)))
		return (char *)'\0';
	i = 0;
	while (str[i] != '\0')
	{
		newstr[i] = str[i];
		i++;
	}
	newstr[i] = '\0';
	return (newstr);
}
